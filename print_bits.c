#include "libft.h"

void	print_bits(char c, int fd)
{
	int	i;

	i = 7;
	while (i >= 0)
	{
		if (c >> i & 1)
			write(fd, "1", 1);
		else
			write(fd, "0", 1);
		i--;
	}
}
